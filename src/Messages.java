    private void addmessage(){
    int messageId = Integer.parseInt(txtmessageid.getText());
    int senderId = Integer.parseInt(txtsenderid.getText());
    int receiverId = Integer.parseInt(txtrecieverid.getText());
    String content = txtcontent.getText();
    String timestamp = txttimestamp.getText();
    
    // Create a document to insert into the collection
    Document d = new Document("messageId", messageId);
    d.append("senderId", senderId);
    d.append("receiverId", receiverId);
    d.append("content", content);
    d.append("timestamp", timestamp);
    
    // Insert the document into the collection
    collection.insertOne(d);
    
    // Show a success message
    JOptionPane.showMessageDialog(this, "Message inserted successfully");

}
