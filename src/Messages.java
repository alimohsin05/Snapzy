private void refreshDocumentDisplay() {
    StringBuilder sb = new StringBuilder();

    for (Document doc : collection.find()) {
        sb.append("Message ID: ").append(doc.getInteger("messageId")).append("\n");
        sb.append("Sender ID: ").append(doc.getInteger("senderId")).append("\n");
        sb.append("Receiver ID: ").append(doc.getInteger("receiverId")).append("\n");
        sb.append("Content: ").append(doc.getString("content")).append("\n");
        sb.append("Timestamp: ").append(doc.getString("timestamp")).append("\n");
        sb.append("--------------------------------------------------\n");
    }

    DocumentViewer.setText(sb.toString());
}
