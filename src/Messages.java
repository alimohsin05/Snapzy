private void updatemessage() {
    // Convert input values to appropriate types
    int messageId = Integer.parseInt(txtmessageid.getText());
    int senderId = Integer.parseInt(txtsenderid.getText());
    int receiverId = Integer.parseInt(txtrecieverid.getText());
    String content = txtcontent.getText();
    String timestamp = txttimestamp.getText();
    
    // Create a query to find the message by messageId
    Document query = new Document("messageId", messageId);
    
    // Create an update document with the new message details
    Document update = new Document("$set", new Document("senderId", senderId)
                                              .append("receiverId", receiverId)
                                              .append("content", content)
                                              .append("timestamp", timestamp));
    
    // Debugging output
    System.out.println("Query: " + query.toJson());
    System.out.println("Update: " + update.toJson());
    
    // Perform the update operation
    long modifiedCount = collection.updateOne(query, update).getModifiedCount();
    
    // Check if the document was updated
    if (modifiedCount > 0) {
        JOptionPane.showMessageDialog(this, "Message updated successfully");
    } else {
        JOptionPane.showMessageDialog(this, "No message found with Message ID: " + messageId);
    }
}
