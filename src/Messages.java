private void searchMessageById(int messageId) {
    // Define the search query
    Document query = new Document("messageId", messageId);
   
    Document foundMessage = collection.find(query).first();
    
    // Check if a message with the given ID exists
    if (foundMessage != null) {
        // Populate the text fields with the found message data
        txtmessageid.setText(String.valueOf(foundMessage.getInteger("messageId")));
        txtsenderid.setText(String.valueOf(foundMessage.getInteger("senderId")));
        txtrecieverid.setText(String.valueOf(foundMessage.getInteger("receiverId")));
        txtcontent.setText(foundMessage.getString("content"));
        txttimestamp.setText(foundMessage.getString("timestamp"));
    } else {
        
        txtsenderid.setText("");
        txtrecieverid.setText("");
        txtcontent.setText("");
        txttimestamp.setText("");
        JOptionPane.showMessageDialog(this, "No message found with Message ID: " + messageId);
    }
}
