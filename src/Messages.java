private void deleteMessageById(int messageId) {
    // Create a query to find the message by messageId
    Document query = new Document("messageId", messageId);
    
    // Perform the deletion operation
    DeleteResult result = collection.deleteOne(query);
    
    // Check if any message was deleted
    if (result.getDeletedCount() > 0) {
        JOptionPane.showMessageDialog(this, "Message with Message ID " + messageId + " deleted successfully.");
        
        // Clear the text fields
        txtmessageid.setText("");
        txtsenderid.setText("");
        txtrecieverid.setText("");
        txtcontent.setText("");
        txttimestamp.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "No message found with Message ID: " + messageId);
    }
}
