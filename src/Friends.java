    private void refreshDocumentDisplay() {
    StringBuilder sb = new StringBuilder();

    for (Document doc : collection.find()) {
        sb.append("Friendship ID: ").append(doc.getInteger("friendshipId")).append("\n");
        sb.append("User ID: ").append(doc.getInteger("userId")).append("\n");
        sb.append("Friend ID: ").append(doc.getInteger("friendId")).append("\n");
        sb.append("Status: ").append(doc.getString("status")).append("\n");
        sb.append("--------------------------------------------------\n");
    }
    DocumentViewer.setText(sb.toString());
}
    
