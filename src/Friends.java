private void deleteFriendshipById(int friendshipId) {
    
    Document query = new Document("friendshipId", friendshipId);
    
    DeleteResult result = collection.deleteOne(query);
    
    
    if (result.getDeletedCount() > 0) {
        JOptionPane.showMessageDialog(this, "Friendship record with Friendship ID " + friendshipId + " deleted successfully.");
        
    } else {
        JOptionPane.showMessageDialog(this, "No friendship record found with Friendship ID: " + friendshipId);
        txtfriendshipid.setText("");
        txtuserid.setText("");
        txtfriendid.setText("");
        txtstatus.setText("");
    }
}

