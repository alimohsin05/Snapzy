    private void addbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbuttonActionPerformed
        // TODO add your handling code here:
        
        int friendshipId = Integer.parseInt(txtfriendshipid.getText());
        int userId = Integer.parseInt(txtuserid.getText());
        int friendId = Integer.parseInt(txtfriendid.getText());
        String status = txtstatus.getText();

        Document friendDocument = new Document("friendshipId", friendshipId)
                                        .append("userId", userId)
                                        .append("friendId", friendId)
                                        .append("status", status);

        collection.insertOne(friendDocument);
        JOptionPane.showMessageDialog(this, "Friends record inserted successfully");

        refreshDocumentDisplay();
    }//GEN-LAST:event_addbuttonActionPerformed


    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        // TODO add your handling code here:
    int friendshipId = Integer.parseInt(txtfriendshipid.getText());
    int userId = Integer.parseInt(txtuserid.getText());
    int friendId = Integer.parseInt(txtfriendid.getText());
    String status = txtstatus.getText();

    Document query = new Document("friendshipId", friendshipId);
    
    Document update = new Document("$set", new Document("userId", userId)
                                            .append("friendId", friendId)
                                            .append("status", status));

   
    UpdateResult result = collection.updateOne(query, update);
    
    if (result.getModifiedCount() > 0) {
        JOptionPane.showMessageDialog(this, "Friends record updated successfully");
    } else {
        JOptionPane.showMessageDialog(this, "No friends record found with Friendship ID: " + friendshipId);
        txtfriendshipid.setText("");
        txtuserid.setText("");
        txtfriendid.setText("");
        txtstatus.setText("");
    }
    
    refreshDocumentDisplay();
    }//GEN-LAST:event_updatebuttonActionPerformed

private void searchFriendshipById(int friendshipId) {
    // Define the search query
    Document query = new Document("friendshipId", friendshipId);
    
    // Perform the search
    Document foundFriendship = collection.find(query).first();
    
    if (foundFriendship != null) {
        txtfriendshipid.setText(String.valueOf(foundFriendship.getInteger("friendshipId")));
        txtuserid.setText(String.valueOf(foundFriendship.getInteger("userId")));
        txtfriendid.setText(String.valueOf(foundFriendship.getInteger("friendId")));
        txtstatus.setText(foundFriendship.getString("status"));
    } else {
        // Clear the text fields
        txtfriendshipid.setText("");
        txtuserid.setText("");
        txtfriendid.setText("");
        txtstatus.setText("");
        // If no friendship record found with the given ID, ye show hoga
        JOptionPane.showMessageDialog(this, "No friends record found with Friendship ID: " + friendshipId);
    }
}

